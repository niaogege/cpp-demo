// 设计一个函数传入两个dom节点 找出最近的父节点
// 递归
/**
 * @author: Chendapeng
 * @description: 
 * @param {*} node1
 * @param {*} node2
 * @return {*}
 */
const findCommonParentNode = (node1, node2) => {
  if (node1.contains(node2)) {
    return node1
  } else if (node2.contains(node1)) {
    return node2
  } else {
    //从一个节点的父级开始查找 直到找到一个共同的祖先节点
    findCommonParentNode(node1.parentNode, node2)
  }
}
