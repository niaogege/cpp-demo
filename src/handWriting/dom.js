/**
 * @author: Chendapeng
 * @description: 
 * @param {*} node1
 * @param {*} node2
 * @return {*}
 */
const findCommonParent = (node1, node2) => {
  while(!node1.contains(node2)) {
    node1 = node1.parentNode
  }
  return node1
}